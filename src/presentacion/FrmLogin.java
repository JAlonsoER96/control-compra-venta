    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if (txtEmail.getText().length()==0 || txtEmail.getText().length()>50) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un email valido","Sistema",JOptionPane.WARNING_MESSAGE);
            txtEmail.requestFocus();
            System.exit(0);
        }
        if (txtPswd.getText().length()==0 || txtPswd.getText().length()>64) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un password valido","Sistema",JOptionPane.WARNING_MESSAGE);
            txtPswd.requestFocus();
            System.exit(0);
        }
        ControlUsuario cu = new ControlUsuario();
        String respuesta = cu.login(txtEmail.getText(), txtPswd.getText());
        if (respuesta.equals("1")) {
         this.dispose();
         FrmPrincipal frmp = new FrmPrincipal();
         frmp.toFront();
         frmp.setVisible(true);
        }else if(respuesta.equals("2")){
            JOptionPane.showMessageDialog(this, "El usuario no tiene acceso","Sistema",JOptionPane.WARNING_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Los datos de acceso no son correctos","Sistema",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

