    private void listar() {
        Calendar cal;
        Date fechaIn;
        Date fechaFin;
        int dia,mes,año;
        cal = dcInicio.getCalendar();
        dia = cal.get(Calendar.DAY_OF_MONTH);
        mes = cal.get(Calendar.MONTH);
        año = cal.get(Calendar.YEAR)-1900;
        fechaIn = new Date(año,mes,dia);
        cal = dcFin.getCalendar();
        dia = cal.get(Calendar.DAY_OF_MONTH);
        mes = cal.get(Calendar.MONTH);
        año = cal.get(Calendar.YEAR)-1900;
        fechaFin = new Date(año,mes,dia);
        tablaListIngreso.setModel(this.CONTROL.consultaFechas(fechaIn, fechaFin));
        TableRowSorter orden = new TableRowSorter(tablaListIngreso.getModel());
        this.ocultarColumnas();
        tablaListIngreso.setRowSorter(orden);
        lblTotalRegistros.setText("Mostrando: " + this.CONTROL.showAll() + " de un total de: " + this.CONTROL.count() + " registros");
    }

